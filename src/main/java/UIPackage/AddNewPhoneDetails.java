    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         boolean Valid = false;
           if (txtPhoneId.getText().isEmpty() ||txtBrand.getText().isEmpty() ||txtName.getText().isEmpty()|| txtOs.getText().isEmpty()
                   ||txtDisplay.getText().isEmpty()||txtCamera.getText().isEmpty()||txtProcessor.getText().isEmpty()
                   ||txtRam.getText().isEmpty()||txtStorage.getText().isEmpty()||txtBattery.getText().isEmpty()
                   ||txtPrice.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Field cannot be blank." ,"Add Details", 
                    JOptionPane.ERROR_MESSAGE);
        } 
            else 
        {
    String PhoneID=txtPhoneId.getText();    
    String BrandName=txtBrand.getText();
    String Name=txtName.getText();
    String OS=txtOs.getText();
    String Display=txtDisplay.getText();
    String Camera=txtCamera.getText();
    String Processor=txtProcessor.getText();
    String RAM=txtRam.getText();
    String Storage=txtStorage.getText();
    String Battery=txtBattery.getText();
    String Price=txtPrice.getText();
    String Category= cmbCategory.getSelectedItem().toString(); 
            
            PhoneDetails pdetails = new PhoneDetails(PhoneID,BrandName,Name,OS,Display,Camera,Processor,RAM,Storage,
            Battery,Price,Category);
             
            if (pdetails.addNewPhone()) {
                JOptionPane.showMessageDialog(null,"Add Successfull" ,"Add Details ",
                        JOptionPane.INFORMATION_MESSAGE);
                
               
                
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Add Fail" ,"Add Details", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

