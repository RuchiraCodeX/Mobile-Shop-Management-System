    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
      String keyword = txtPhoneId.getText();
        
        if (keyword.isEmpty()) 
        {
        JOptionPane.showMessageDialog(null,"Sorry! Have to enter Brand Name" ,
                "Search a Phones", JOptionPane.ERROR_MESSAGE);
        
        } 
        else
        {
            PhoneDetails Ph = new PhoneDetails();
String TableTitle = "PhoneID \t Brand \t Name \t OS \t Display \t Camera \t Processor \t RAM \t Storage \t Battery \t Price \t Category\n------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------- \n";
            if (Ph.SearchPhones(keyword))
            {
                txtPhoneDetails.setText(TableTitle+Ph.getBrandName()+"\t"+Ph.getName()+"\t"+ Ph.getOS()+"\t"+Ph.getDisplay()+"\t"+Ph.getCamera()+"\t"+ Ph.getProcessor()+"\t"+Ph.getRAM()+"\t"+Ph.getStorage()+"\t"+Ph.getBattery()+"\t"+Ph.getPrice()+"\t"+Ph.getCategory());
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"Sorry! We do not have any matching Phone" ,
                        "Search a Phones", 
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        
      
    }//GEN-LAST:event_btnSearchActionPerformed

